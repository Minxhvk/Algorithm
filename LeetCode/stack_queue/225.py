import collections
from gc import collect


class MyStack:

    def __init__(self):
        self.ls = []

    def push(self, x: int) -> None:
        self.ls.append(x)

    def pop(self) -> int:
        return self.ls.pop()

    def top(self) -> int:
        return self.ls[-1]

    def empty(self) -> bool:
        if len(self.ls) < 1:
            return True
        else:
            return False


